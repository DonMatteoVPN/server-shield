# –§—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –û–°
check_os() {
    if [[ -f /etc/debian_version ]]; then
        export OS="debian"
    elif [[ -f /etc/redhat-release ]]; then
