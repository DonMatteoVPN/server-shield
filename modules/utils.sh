# –§—É–Ω–∫—Ü–∏—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–æ–Ω—Ñ–∏–≥–∞
save_config() {
    local key="$1"
    local value="$2"
    
    # –°–æ–∑–¥–∞—ë–º —Ñ–∞–π–ª –µ—Å–ª–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
    touch "$SHIELD_CONFIG"
    
    # –£–¥–∞–ª—è–µ–º —Å—Ç–∞—Ä–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –µ—Å–ª–∏ –µ—Å—Ç—å
    sed -i "/^${key}=/d" "$SHIELD_CONFIG"
    
    # –î–æ–±–∞–≤–ª—è–µ–º –Ω–æ–≤–æ–µ
    echo "${key}=${value}" >> "$SHIELD_CONFIG"
}

# –§—É–Ω–∫—Ü–∏—è —á—Ç–µ–Ω–∏—è –∫–æ–Ω—Ñ–∏–≥–∞
get_config() {
    local key="$1"
    local default="$2"
    
    if [[ -f "$SHIELD_CONFIG" ]]; then
        local value=$(grep "^${key}=" "$SHIELD_CONFIG" | cut -d'=' -f2-)
        if [[ -n "$value" ]]; then
            echo "$value"
            return
        fi
    fi
    echo "$default"
}

# –§—É–Ω–∫—Ü–∏—è –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è
confirm() {
    local prompt="$1"
    local default="${2:-n}"
    
    if [[ "$default" == "y" ]]; then
        prompt="$prompt [Y/n]: "
    else
        prompt="$prompt [y/N]: "
    fi
    
    read -p "$prompt" response
    response=${response:-$default}
    
    [[ "$response" =~ ^[Yy]$ ]]
}

